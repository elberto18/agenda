    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
       ContGuardados = miAgenda.abrirAgenda();
       int numeroContactos = ContGuardados.size();
       System.out.println("Numero de contactos " + numeroContactos);
       modelo1.setRowCount(ContGuardados.size());
       
       for(int i=0;i<numeroContactos; i++){
       String[] ContTabla = ContGuardados.get(i).toString().split(",",8);
       for (int j=0; j<8 ; j++){
       modelo1.setValueAt(ContTabla[j], i, j);
       }
       }
    }//GEN-LAST:event_jButton1ActionPerformed

