    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String texto = jTextField2.getText();
        String Categoria = null;
        if(null != texto)switch (texto) {
        case "proveedores":
        case "Proveedores":
        case "PROVEEDORES":
            Categoria = "1";
            break;
        case "Cliente":
        case "cliente":
        case "CLIENTES":
            Categoria = "2";
            break;
        case "Empleado":
        case "empleado":
        case "EMPLEADO":
            Categoria = "3";
            break;
        default:
            break;}
        
            String Nombre = jTextField3.getText();
            String Apellidos = jTextField4.getText();
            String Direccion = jTextField5.getText();
            String zipCode = jTextField6.getText();
            String ciudad = jTextField7.getText();
            String NumeroTelefono = jTextField8.getText();
            String Email = jTextField9.getText();
            miAgenda.agregarContacto(Categoria, Nombre, Apellidos, Direccion, zipCode, ciudad, NumeroTelefono, Email);
    
        
    }//GEN-LAST:event_jButton3ActionPerformed

